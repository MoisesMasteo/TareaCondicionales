Primer_numero = int(input("Cuál es el primer nñúmero? "))
Segundo_numero = int(input("Cuál es el segundo número? "))
if (Primer_numero == Segundo_numero):
    print("Las dos números son iguales.")
elif (Primer_numero > Segundo_numero):
    print("El número más pequeño es :", Segundo_numero)
else:
    print("El número más pequeño es:", Primer_numero)
